package Personal_Practice;

import java.util.Scanner;

public class DemoStr3 {
    public static void main(String[] args) {
        String str3 = "Java programming";
        System.out.println("Length of the string is: " + str3.length());
        System.out.println("First Character: " + str3.charAt(0));

        System.out.println("Character: " + str3.charAt(5));

        System.out.println("Last Character: " + str3.charAt(str3.length() - 1) );
    }
}
